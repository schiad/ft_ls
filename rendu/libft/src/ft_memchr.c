#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char *p;

	p = (unsigned char *)s;
	if (n)
	{
		while (n)
		{
			if (*p == (unsigned char)c)
				return ((void *)(p));
			n--;
			p++;
		}
	}
	else
		return (NULL);
	return (NULL);
}
